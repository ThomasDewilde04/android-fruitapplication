package com.example.fruitapplication.ui.util

/**
 * Enum class representing different types of navigation used in the Fruit Application.
 */
enum class FruitNavigationType {
    /**
     * Bottom navigation style where tabs are placed at the bottom of the screen.
     */
    BOTTOM_NAVIGATION,

    /**
     * Navigation rail style providing a sidebar for navigation options.
     */
    NAVIGATION_RAIL,

    /**
     * Permanent navigation drawer style offering a persistent sidebar for navigation.
     */
    PERMANENT_NAVIGATION_DRAWER
}